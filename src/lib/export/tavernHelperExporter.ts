function getTypeDefault(type: string): unknown {
  switch (type) {
    case 'number': return 0;
    case 'boolean': return false;
    case 'string': return '';
    case 'array': return [];
    case 'record': return {};
    case 'object': return {};
    default: return null;
  }
}

// ─── jsdelivr Linker ────────────────────────────────────────────────────

export function generateJsdelivrLinks(
  owner: string,
  repo: string,
  files: string[],
  branch = 'main',
): Array<{ file: string; url: string; scriptTag: string }> {
  return files.map(file => {
    const url = `https://testingcf.jsdelivr.net/gh/${owner}/${repo}@${branch}/${file}`;
    const ext = file.split('.').pop();
    let scriptTag: string;

    if (ext === 'js' || ext === 'ts') {
      scriptTag = `<script type="module" src="${url}"></script>`;
    } else if (ext === 'css') {
      scriptTag = `<link rel="stylesheet" href="${url}">`;
    } else {
      scriptTag = `<!-- ${url} -->`;
    }

    return { file, url, scriptTag };
  });
}

export function generateAutoLoaderScript(
  owner: string,
  repo: string,
  branch = 'main',
): string {
  return `<!-- Tavern Card Studio Auto-Loader -->
<!-- Paste this into your card's HTML/body section -->
<body>
<script>
(async () => {
  const base = 'https://testingcf.jsdelivr.net/gh/${owner}/${repo}@${branch}/dist';
  
  // Load CSS
  const link = document.createElement('link');
  link.rel = 'stylesheet';
  link.href = \`\${base}/styles.css\`;
  document.head.appendChild(link);
  
  // Load main script
  const script = document.createElement('script');
  script.type = 'module';
  script.src = \`\${base}/index.ts\`;
  document.body.appendChild(script);
})();
</script>
</body>`;
}

