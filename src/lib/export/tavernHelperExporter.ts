function generateSyncYaml(owner: string, repo: string, filePaths: string[]): string {
  return [
    '# Tavern Card Studio — Auto-sync configuration',
    '# This file configures jsdelivr CDN auto-update',
    '',
    'update_strategy: jsdelivr',
    `jsdelivr_base: "https://testingcf.jsdelivr.net/gh/${owner}/${repo}"`,
    '',
    'files:',
    ...filePaths.filter(p => p.startsWith('dist/')).map(p => `  - ${p}`),
    '',
    '# GitHub Actions will auto-build and push to jsdelivr',
    '# See: https://github.com/StageDog/tavern_helper_template',
  ].join('\n');
}

